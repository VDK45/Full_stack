"""
    5. Реализовать формирование списка, используя функцию range()
    и возможности генератора. 
    В список должны войти четные числа от 100 до 1000 (включая границы). 
    Необходимо получить результат вычисления произведения всех элементов списка.
    Подсказка: использовать функцию reduce().
"""

from functools import reduce
# Пример reduce:

def reducer_func(el_prev, el):
        # el_prev — предшествующий элемент
        # el — текущий элемент
        return el_prev + el
    
print(reduce(reducer_func, [1, 2, 3]))  # 6

# Решение:

def mul_all(el, el_next):
    return el * el_next

uniq_lst = [el for el in range(100, 1001, 2)] # 1001 (до 1000 включая границы)
new_lst = reduce(mul_all, uniq_lst)
print(new_lst)

# Вариант решения -----------

my_list = [a for a in range(100, 1001) if a % 2 == 0]

def my_func(prev_i, i):
    return prev_i * i

print(reduce(my_func, my_list))



